export function matchFAQ(query: string): string | null {
  const normalizedQuery = query.toLowerCase().trim();

  // Navigation-specific shortcuts
  if (normalizedQuery.includes('show gallery') || normalizedQuery === 'gallery') {
    return aiFAQData['show gallery'];
  }
  if (normalizedQuery.includes('i want to donate') || normalizedQuery === 'donate') {
    return aiFAQData['i want to donate'];
  }
  if (normalizedQuery.includes('upcoming hike') || normalizedQuery.includes('next hike')) {
    return aiFAQData['upcoming hike'];
  }
  if (normalizedQuery.includes('contact') || normalizedQuery.includes('reach out') || normalizedQuery.includes('get in touch')) {
    return aiFAQData['contact'];
  }
  if (normalizedQuery.includes('sponsor')) {
    return aiFAQData['sponsors'];
  }

  // Check if we should redirect to contact for bookings/pricing
  if (contactRedirectPatterns.some(pattern => normalizedQuery.includes(pattern))) {
    return `I'd be happy to help with specific bookings, pricing, or custom requests. Please contact us directly:\n\n- **Email:** [acharyaraj2005@gmail.com](mailto:acharyaraj2005@gmail.com)\n- **Phone:** [98767262762](tel:+9779876726276)\n\n➡️ [Contact Page](/contact)`;
  }

  // Direct match
  if (aiFAQData[normalizedQuery]) {
    return aiFAQData[normalizedQuery];
  }

  // Keyword matching
  for (const [key, patterns] of Object.entries(keywordPatterns)) {
    if (patterns.some(pattern => normalizedQuery.includes(pattern))) {
      const faqKey = key.replace(/_/g, ' ');
      for (const [dataKey, dataValue] of Object.entries(aiFAQData)) {
        if (dataKey.includes(faqKey) || faqKey.includes(dataKey)) {
          return dataValue;
        }
      }
    }
  }

  return null;
}

